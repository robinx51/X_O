package com.x_o_client.data;

public class ResponseData {
    private String stringValue;
    private String otherStringValue;
    private int intValue;
    private boolean boolValue;

    public String getStringValue() { return stringValue; }
    public void setStringValue(String value) { stringValue = value; }
    public String getOtherStringValue() { return otherStringValue; }
    public void setOtherStringValue(String value) { otherStringValue = value; }
    public int getIntValue() { return intValue; }
    public void setIntValue(int value) { intValue = value; }
    public boolean getBoolValue() { return boolValue; }
    public void setBoolValue(boolean value) { boolValue = value; }
}
